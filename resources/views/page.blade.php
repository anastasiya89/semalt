<H1>1) Написать алгоритм решения задачи:</H1>
    <p>
    В классе 28 учеников. 75% из них занимаются спортом. Сколько учеников в классе
    занимаются спортом и сколько всего учеников в классе?</p>

    <h3>Код решения</h3>
    <pre>   
    $sum_learner = 28;
    $pct_play_sports = 75;
    $sum_play_sports = $sum_learner * $pct_play_sports / 100;
    echo ...</pre>

    <h3>Результат выполнения</h3>
    <p>Всего учеников в классе — {{$sum_learner}}<br>
        занимаются спортом — {{$sum_play_sports}}</p>

