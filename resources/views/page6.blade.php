	<H1>6) Чем отличается оператор == от === ?</H1>

	<h3>Ответ</h3>

	<pre>Оператор == сравнивает значения после преобразования типов. 
	То есть: 
	if (true == 1){
		echo 'true';
	} // true.
	А оператор '===' определяет полное сходство (значение и тип).
	То есть: true === 1 // false.</pre>
