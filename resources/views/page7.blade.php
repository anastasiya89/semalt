	<H1>7) Чем отличается require от include ?</H1>

	<h3>Ответ</h3>

	<pre>
	Отличия между require() и include() в том, что если файл не найден
	require() возвращает FATAL ERROR и скрипт прекращает работать,
	include() возвращает WARNING и скрипт продолжает работать.</pre>
